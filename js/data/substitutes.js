/* ─────────────────────────────────────────────────────────
   Пошук замінників за назвою інгредієнта і ціллю
   ───────────────────────────────────────────────────────── */

/** Нормалізує рядок для порівняння */
function norm(s) {
  return s.toLowerCase().trim();
}

/**
 * Повертає масив замінників для даного інгредієнта і цілі.
 * @param {string} itemName  — назва інгредієнта з плану
 * @param {string} goal      — 'cut' | 'bulk' | 'recomp'
 * @returns {Array|null}
 */
export function findSubs(itemName, goal) {
  const n = norm(itemName);
  for (const entry of SUBS_DB) {
    if (entry.names.some(name => n.includes(norm(name)) || norm(name).includes(n))) {
      return entry.subs[goal] ?? entry.subs.recomp ?? null;
    }
  }
  return null;
}